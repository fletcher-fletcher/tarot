"""Database of all 78 Tarot cards with complete descriptions"""

from typing import Dict, List, Optional


class TarotCard:
    """Tarot card data structure"""
    
    def __init__(
        self,
        card_id: str,
        name_ru: str,
        name_en: str,
        arcana: str,
        number: int,
        short_upright: str,
        short_reversed: str,
        keywords_upright: List[str],
        keywords_reversed: List[str],
        meaning_upright: Dict[str, str],
        meaning_reversed: Dict[str, str],
        advice: str,
        warning: str,
        description: str = "",
        image: str = "",
    ):
        self.card_id = card_id
        self.name_ru = name_ru
        self.name_en = name_en
        self.arcana = arcana
        self.number = number
        self.short_upright = short_upright
        self.short_reversed = short_reversed
        self.keywords_upright = keywords_upright
        self.keywords_reversed = keywords_reversed
        self.meaning_upright = meaning_upright
        self.meaning_reversed = meaning_reversed
        self.advice = advice
        self.warning = warning
        self.description = description
        self.image = image
        self.is_reversed = False


